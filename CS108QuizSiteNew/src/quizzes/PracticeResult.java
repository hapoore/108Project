package quizzes;

import java.util.Date;

public class PracticeResult {
	public String quizName;
	public String user;
	public double averageRepetitions;
	public Date date;
	public double timeNeeded;
	
	//	QuizResult result = new QuizResult(String user,int score,double percentage,Date time, int timeNeeded);
	
	public PracticeResult(String quizName, String user, double averageRepetitions, Date date, double timeNeeded){
		this.quizName = quizName;
		this.user = user;
		this.averageRepetitions = averageRepetitions;
		this.date = date;
		this.timeNeeded = timeNeeded;
	}
}
